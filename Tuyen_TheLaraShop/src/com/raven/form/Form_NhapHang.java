    public boolean checktable() {
        DecimalFormat format = new DecimalFormat("#,###,##0.0");
        for (int i = 0; i < tblList.getRowCount(); i++) {
            try {
//                slStr = (String) tblList.getValueAt(i, 3);
//                int sl = ()Integer.parseInt((String) tblList.getValueAt(i, 3));
                int sl = (int) Integer.valueOf(tblList.getValueAt(i, 3).toString());
                if (sl < 1) {
                    this.showMessage("Số lượng ở hàng thứ " + (++i) + " phải lớn hơn 1.");
                    return false;
                }
                if (!tblList.getValueAt(i, 4).toString().matches("^[1-9]{0,1}[0-9]{0,2}\\.[[0-9]{3}\\.]+,0")) {
                    this.showMessage("Giá ở hàng thứ " + (++i) + " không đúng định dạng '#.###,0'");
                    return false;
                } else {
                    Number giaformat = format.parse((String) tblList.getValueAt(i, 4));
                    float gia = giaformat.floatValue();
                    float tongtien = gia * sl;
                    String tongtienfomat = format.format(tongtien);
                    model.setValueAt(tongtienfomat, i, 5);
                    setForm();
                }
            } catch (Exception e) {
                this.showMessage("Số lượng ở hàng thứ " + (++i) + " sai định dạng số.");
                return false;
            }
        }
        return true;
    }

