    public void FillSP(SanPham sp, JPanel pnel, JLabel hinh, JLabel maSP, JLabel ten, JLabel gia, JLabel SlKho, JComboBox cbxmau) {
        pnel.setVisible(true);
        setHinhAnh(sp.getHinh(), hinh);
        maSP.setText(sp.getMaSP() + "");
        ten.setText(sp.getTenSP());
        DecimalFormat decimalFormat = new DecimalFormat("#,###,##0.0");
        String giaformat = decimalFormat.format(sp.getGiaNhap());
        gia.setText(giaformat);

        DefaultComboBoxModel modelMS = new DefaultComboBoxModel();
        modelMS.removeAllElements();
        List<CTMau> listctm = new ArrayList<>();
        MauSacDAO mauDao = new MauSacDAO();
        CTMDao ctmdao = new CTMDao();
        listctm = ctmdao.selectByMaSP(sp.getMaSP());
        for (CTMau enity : listctm) {
            MauSac mau = mauDao.selectByMaMau(enity.getMaMau());
            modelMS.addElement(mau.getTenMau());
        }
        cbxmau.setModel(modelMS);

        String tenMau = (String) cbxmau.getSelectedItem();
        System.out.println("tên màu:" + tenMau);
        MauSac mau2 = mauDao.selectByTenMau(tenMau);

        for (CTMau cTMau : listKHOTam) {
            if (cTMau.getMaMau() == mau2.getMaMau() && cTMau.getMaSP() == sp.getMaSP()) {
                sp.setSl(cTMau.getSl());
                this.upstatic(sp.getSl(), SlKho);
            }
        }
    }

