    void AddToTable(CTMau enity, JTextField slnhap, JLabel slKho, JComboBox cbxMau) {
        String mau = (String) cbxMau.getSelectedItem();
        if (cbxMau.getSize() == null) {
            mau = "null";
        }
        DecimalFormat decimalFormat = new DecimalFormat("#,###,##0.0");
        if (checkSLNhap(slnhap)) {
            if (Integer.parseInt(slnhap.getText()) != 0 && checkExitSP(enity.getMaSP(), mau) == false) {
                SanPhamDAO spdao = new SanPhamDAO();
                SanPham sp = spdao.selecteByID(enity.getMaSP());
                model.addRow(new Object[]{enity.getMaSP(), sp.getTenSP(), mau, Integer.parseInt(slnhap.getText()), decimalFormat.format(sp.getGiaNhap()), decimalFormat.format(sp.getGiaNhap()* Integer.parseInt(slnhap.getText()))});
                tblList.setModel(model);

                this.setForm();
            }
        }
    }

