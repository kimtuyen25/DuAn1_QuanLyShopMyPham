    JLabel ExitJlabtlKho() {
        int row = tblList.getSelectedRow();
        int maSP = (int) tblList.getValueAt(row, 0);
        if (maSP == this.page * 8 - 7) {
            return SlKho1;
        } else if (maSP == this.page * 8 - 6) {
            return SlKho2;
        } else if (maSP == this.page * 8 - 5) {
            return SlKho3;
        } else if (maSP == this.page * 8 - 4) {
            return SlKho4;
        } else if (maSP == this.page * 8 - 3) {
            return SlKho5;
        } else if (maSP == this.page * 8 - 2) {
            return SlKho6;
        } else if (maSP == this.page * 8 - 1) {
            return SlKho7;
        } else if (maSP == this.page * 8) {
            return SlKho8;
        }
        return null;
    }

