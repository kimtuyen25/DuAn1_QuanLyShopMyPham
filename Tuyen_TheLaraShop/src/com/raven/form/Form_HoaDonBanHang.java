    public void setHinhAnh(String path, JLabel hinh) {
        if (path != null) {
            ImageIcon icon = XImage.read(path);
            Image image = icon.getImage();
            Image image2 = image.getScaledInstance(272, 208, Image.SCALE_SMOOTH);
            hinh.setToolTipText(path);
            hinh.setIcon(new ImageIcon(image2));
        } else {
            hinh.setIcon(null);
        }
    }

    private void fillToHDForm() {
        int row = tblHoaDon.getSelectedRow();
        int convertRow = tblHoaDon.convertRowIndexToModel(row);
        if (convertRow >= 0) {
            try {
                Integer IDSanPham = (Integer) tblHoaDon.getValueAt(convertRow, 0);
                List<HoaDon> list = daoHD.getIDHoaDonXuat(IDSanPham);
                for (HoaDon hd : list) {
                    lblMaHD.setText(String.valueOf(hd.getMaHD()));
                    lblNgayTaoHD.setText(hd.getNgayTao());
                    lblNhanVien.setText(String.valueOf(hd.getIDAccount()));
                    lblTenKH.setText(hd.getTenKH());
                    lblTongTien.setText(String.valueOf(String.format("%.1f", hd.getTongTien())));
                }
            } catch (Exception e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(this, "Lỗi truy vẫn dữ liệu");
            }
        }
    }

    private void delete() {

        int luaChon = JOptionPane.showConfirmDialog(this,
                "Bạn thực sự muốn xóa nhân viên này?", "Xác nhận",
                JOptionPane.YES_NO_OPTION);
        if (luaChon == JOptionPane.YES_OPTION) {
            Object maHDObj = (int) tblHoaDon.getValueAt(tblHoaDon.getSelectedRow(), 0);
            int maHD = (int) maHDObj;
            try {
                daoHD.delete(maHD);
                this.fillToTable();
                JOptionPane.showMessageDialog(this, "Xóa thành công.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Xóa thất bại.");
            }
        }
    }
