    private void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);

        try {
            List<HoaDon> list = daoHD.selectHoaDonXuat();
            for (HoaDon hd : list) {
                Object[] row = {
                    hd.getMaHD(),
                    hd.toDate(hd.getNgayTao()),
                    String.format("%.1f", hd.getTongTien()),
                    hd.doubleToInt(hd.getSale() * 100) + "%",
                    String.format("%.1f", hd.getThanhTien()),
                    hd.getIDAccount(),
                    hd.getTenKH()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Lỗi truy vẫn dữ liệu");
        }
    }

